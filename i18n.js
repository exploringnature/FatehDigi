// Current language state
let currentLang = localStorage.getItem('fd_lang') || 'en';

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['en'][key] || key;
}

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('fd_lang', lang);
  document.documentElement.lang = lang;

  // Update all static data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });

  // Update language button label
  const labels = { en: { flag: '🇬🇧', label: 'EN' }, pa: { flag: '🇮🇳', label: 'PA' } };
  document.getElementById('lang-flag').textContent  = labels[lang].flag;
  document.getElementById('lang-label').textContent = labels[lang].label;

  // Mark active dropdown item
  document.querySelectorAll('#lang-dropdown li').forEach(li => {
    li.classList.toggle('active', li.dataset.lang === lang);
  });

  // Re-render dynamic content
  if (typeof renderServices === 'function') renderServices();
  if (typeof renderWhyUs   === 'function') renderWhyUs();
}
